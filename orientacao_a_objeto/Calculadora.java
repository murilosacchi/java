public class Calculadora {
    // método
    // tipo de retorno, nome, lista de parâmetros, corpo
    // há os métodos que produzem valor
    // há os métodos que causam um efeito colateral
    int somar(int a, int b) {
        int resultado = a + b;
        return resultado;
    }

    int subtrair(int a, int b) {
        return a - b;
    }
}
